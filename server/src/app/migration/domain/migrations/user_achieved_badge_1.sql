CREATE TABLE IF NOT EXISTS `user_achieved_badge` (
  id INT NOT NULL AUTO_INCREMENT,
  `user_fk` INT NOT NULL,
  `badge_fk` INT NOT NULL,
  `receipt_place_fk` INT NULL,
  latitude DECIMAL(15,13) NOT NULL,
  longitude DECIMAL(15,13) NOT NULL,
  creation_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  update_date TIMESTAMP NULL DEFAULT NULL,
  PRIMARY KEY(id),
  FOREIGN KEY (`user_fk`) REFERENCES `user`(id) ON UPDATE CASCADE ON DELETE RESTRICT,
  FOREIGN KEY (`badge_fk`) REFERENCES `badge`(id) ON UPDATE CASCADE ON DELETE RESTRICT,
  FOREIGN KEY (`receipt_place_fk`) REFERENCES `place`(id) ON UPDATE CASCADE ON DELETE RESTRICT
)
ENGINE=InnoDB
CHARACTER SET utf8
COLLATE utf8_bin;