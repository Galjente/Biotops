CREATE TABLE IF NOT EXISTS `user_friend` (
  `user_fk` INT NOT NULL,
  `friend_fk` INT NOT NULL,
  FOREIGN KEY (`user_fk`) REFERENCES `user`(id) ON UPDATE CASCADE ON DELETE RESTRICT,
  FOREIGN KEY (`friend_fk`) REFERENCES `user`(id) ON UPDATE CASCADE ON DELETE RESTRICT
)
ENGINE=InnoDB
CHARACTER SET utf8
COLLATE utf8_bin;